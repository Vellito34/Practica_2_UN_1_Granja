public class Ojo {
    String color;
    String tipo;

    public Ojo(String color, String tipo) {
        this.color = color;
        this.tipo = tipo;
    }

    public Ojo(){

    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }


}
