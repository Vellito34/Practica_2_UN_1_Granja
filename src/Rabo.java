public class Rabo {
    String color;
    String tipo;

    public Rabo(String color, String tipo) {
        this.color = color;
        this.tipo = tipo;
    }

    public Rabo(){

    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }



}
